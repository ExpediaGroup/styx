    public static final int INSUFFICIENT_STORAGE = 507;
    public static final int NOT_EXTENDED = 510;
    public static final int NETWORK_AUTHENTICATION_REQUIRED = 511;

    private static final Optional<String>[] NAMES = generateNameOptionals();

    /**
     * Get the name of a status code.
     *
     * @param code code
     * @return name if the code is a known status
     */
    public static Optional<String> name(int code) {
        // Using the array prevents us from needlessly instantiating Optionals at runtime.
        return code < 0 || code >= NAMES.length
                ? Optional.empty()
                : NAMES[code];
    }

    private static Optional<String>[] generateNameOptionals() {
        // most elements will be empty, but since this array is global, the space cost is not significant.
        Optional<String>[] array = new Optional[600];

        // this will only be run at class-load-time, so the wasted iterations are not significant.
        for (int code = 0; code < array.length; code++) {
            array[code] = Optional.ofNullable(name0(code));
        }

        return array;
    }

